/**
 * Single LinkedList
 * -----------------
 * 
 * Single Linked List	
Node Structure			Each node has data and a reference to the next node
Navigation				Can traverse only in one direction (forward)
Memory Usage			Less memory (only one pointer per node)
Insertion/Deletion 		Slower for deleting a node (need to find the previous node)
Use Cases				Simple applications like stacks, queues
 */

package com.raymondchandra.linkedlistconcepts;

public class SingleLinkedList {
	static class Node {
        int data;
        Node next;
        
        Node(int data) {
            this.data = data;
            this.next = null;
        }
    }

    private Node head;

    // Insert at end
    public void insert(int data) {
        Node newNode = new Node(data);
        if (head == null) {
            head = newNode;
            return;
        }
        Node temp = head;
        while (temp.next != null) {
            temp = temp.next;
        }
        temp.next = newNode;
    }

    // Delete a node
    public void delete(int key) {
        if (head == null) return;

        if (head.data == key) {
            head = head.next;
            return;
        }

        Node temp = head;
        while (temp.next != null && temp.next.data != key) {
            temp = temp.next;
        }

        if (temp.next == null) return;
        temp.next = temp.next.next;
    }

    // Display list
    public void display() {
        Node temp = head;
        while (temp != null) {
            System.out.print(temp.data + " -> ");
            temp = temp.next;
        }
        System.out.println("null");
    }

    public static void main(String[] args) {
        SingleLinkedList list = new SingleLinkedList();
        list.insert(10);
        list.insert(20);
        list.insert(30);
        list.display();
        list.delete(20);
        list.display();
    }
}
